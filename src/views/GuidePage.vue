import { ref, onMounted, onUnmounted } from 'vue'

export default {
  name: 'GuidePage',
  setup() {
    const router = useRouter()
    
    // éŸ³é¢‘ç›¸å…³çŠ¶æ€
    const audioPlayer = ref(null)
    const isPlaying = ref(false)
    const volume = ref(0.7)

    // æ’­æ”¾/æš‚åœéŸ³é¢‘
    const toggleAudio = () => {
      if (!audioPlayer.value) return
      
      if (isPlaying.value) {
        audioPlayer.value.pause()
