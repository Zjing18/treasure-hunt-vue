      }
    })

    onUnmounted(() => {
      // æ¸…ç†éŸ³é¢‘èµ„æº
      if (audioPlayer.value) {
        audioPlayer.value.pause()
        audioPlayer.value.currentTime = 0
        audioPlayer.value.removeEventListener('ended', handleAudioEnded)
